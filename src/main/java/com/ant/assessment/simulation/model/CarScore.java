package com.ant.assessment.simulation.model;

public class CarScore {
    private String name;
    private int score;

    public CarScore() {}

    public CarScore(String name, int score) {
        this.name = name;
        this.score = score;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }
}
